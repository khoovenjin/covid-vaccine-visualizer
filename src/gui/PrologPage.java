    private void printOutput(String message) {
        String currentMessage = plglogarea.getText();
        if (currentMessage.isEmpty()) {
            currentMessage += message;
        } else {
            currentMessage += "\n" + message;
        }
        plglogarea.setText(currentMessage);
    }

