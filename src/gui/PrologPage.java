    private void plgsortbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_plgsortbtnActionPerformed
        String connection = PrologStatement.connectPLFile(PrologStatement.KNOWLEDGEBASE_FILE);
        int selection = plgsortcombobox.getSelectedIndex();
        
        switch(selection){
            case 0: {
                plglogarea.setText("");
                Font bigFont = new Font("calibri", Font.ITALIC, 18);
                plglogarea.setFont(bigFont);
                printOutput(connection + "\n");
                printOutput("-------- [Total of Vaccination Registration by States in Ascending Order] --------");
                
                
                List<String> result = PrologStatement.TotalRegistrationInAscendingOrder();
                result.forEach((tempstring) -> {
                    printOutput(tempstring);
                });
                
                printOutput("____________________________ [End] ____________________________");
                break;
            }
            case 1: {
                plglogarea.setText("");
                Font bigFont = new Font("calibri", Font.ITALIC, 18);
                plglogarea.setFont(bigFont);
                printOutput(connection + "\n");
                printOutput("-------- [Total of Vaccination Registration by States in Descending Order] --------");
                
                List<String> result = PrologStatement.TotalRegistrationInDescendingOrder();
                result.forEach((tempstring) -> {
                    printOutput(tempstring);
                });
                printOutput("____________________________ [End] ____________________________");
                break;
            }
        }
    }//GEN-LAST:event_plgsortbtnActionPerformed

